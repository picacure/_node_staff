(function (window) {
    var $out = "";
    $out += '<!DOCTYPE html>\r\n<html>\r\n<head>\r\n    <title></title>\r\n</head>\r\n';

    window.TMPL = {};
    window.TMPL.head = $out;
})(window);
(function (window) {
    var $out = "";
    $out += '<body>\r\n\r\n</body>';

    window.TMPL.body = $out;
})(window);
(function (window) {
    var $out = "";
    $out += '</html>';

    window.TMPL.footer = $out;
})(window);
